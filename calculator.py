# 1.我要解决什么问题？
# （让用户输入两个数字，程序打出加减乘除的结果）
# 2.输入是什么，输出是什么？
# （输入两个数字，输出两个数字经过加减乘除后得出的结果）
# 3.中间步骤：
# （拿到第一个数字，拿到第二个数字，把文字变数字float(),做加减乘除，把结果打印出来，注意除数不为零
# 4.开始写代码：
# 1.获取用户输入
# 2.转换成数字
# 3.计算加减乘除
# 4.得出结果
a = float(input("请输入第一个数字:"))
b = float(input("请输入第二个数字:"))
print(f"{a}+{b}={a+b}")
print(f"{a}-{b}={a-b}")
print(f"{a}*{b}={a*b}")
if b != 0:
    print(f"{a}/{b}={a/b}")
else:
    print("除数不能为0")
